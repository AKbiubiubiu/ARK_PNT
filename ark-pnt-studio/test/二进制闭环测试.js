console.log('测试 1：256×256 全红像素');
{
  const 宽度 = 256, 高度 = 256;
  const 原始数据 = new Uint8ClampedArray(宽度 * 高度 * 4);
  for (let i = 0; i < 原始数据.length; i += 4) {
    原始数据[i+0] = 255; 原始数据[i+1] = 0; 原始数据[i+2] = 0; 原始数据[i+3] = 255;
  }
  const 字节流 = 生成Pnt字节流({ 宽度, 高度, 数据: 原始数据 });
  断言(字节流.byteLength === 8 + 256*256*4, `文件大小 ${字节流.byteLength} 字节`);
  const 视图 = new DataView(字节流);
  断言(视图.getUint32(0, true) === 256, '头部宽度 256');
  断言(视图.getUint32(4, true) === 256, '头部高度 256');
  const 字节数组 = new Uint8Array(字节流);
  断言(字节数组[8] === 0 && 字节数组[9] === 0 && 字节数组[10] === 255 && 字节数组[11] === 255, 'BGRA 顺序正确');
  const 解析 = 解析Pnt字节流(字节流);
  let ok = true;
  for (let i = 0; i < 原始数据.length; i++) if (解析.数据[i] !== 原始数据[i]) { ok = false; break; }
  断言(ok, '256×256 全红像素 100% 还原');
}

