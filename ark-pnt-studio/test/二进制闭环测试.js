// ===== 二进制引擎核心 / Binary engine core =====

function 生成Pnt字节流(像素矩阵) {
  const { 宽度, 高度, 数据 } = 像素矩阵;
  if (!像素矩阵) throw new Error('空');
  if (宽度 <= 0 || 高度 <= 0) throw new Error('尺寸无效');
  if (!数据 || 数据.length !== 宽度 * 高度 * 4) throw new Error('长度不匹配');

  const 头部字节数 = 8;
  const 总字节数 = 头部字节数 + 宽度 * 高度 * 4;
  const 缓冲区 = new ArrayBuffer(总字节数);
  const 视图 = new DataView(缓冲区);
  const 字节数组 = new Uint8Array(缓冲区);

  视图.setUint32(0, 宽度, true);
  视图.setUint32(4, 高度, true);

  for (let i = 0; i < 数据.length; i += 4) {
    const 偏移 = 头部字节数 + i;
    字节数组[偏移 + 0] = 数据[i + 2]; // B
    字节数组[偏移 + 1] = 数据[i + 1]; // G
    字节数组[偏移 + 2] = 数据[i + 0]; // R
    字节数组[偏移 + 3] = 数据[i + 3]; // A
  }
  return 缓冲区;
}

function 解析Pnt字节流(缓冲区) {
  if (!缓冲区 || 缓冲区.byteLength < 8) throw new Error('文件过小');
  const 视图 = new DataView(缓冲区);
  const 宽度 = 视图.getUint32(0, true);
  const 高度 = 视图.getUint32(4, true);
  if (宽度 <= 0 || 高度 <= 0 || 宽度 > 4096 || 高度 > 4096) throw new Error('尺寸异常');

  const 头部字节数 = 8;
  const 预期像素字节数 = 宽度 * 高度 * 4;
  if (缓冲区.byteLength < 头部字节数 + 预期像素字节数) throw new Error('文件截断');

  const 字节数组 = new Uint8Array(缓冲区);
  const 像素数据 = new Uint8ClampedArray(预期像素字节数);

  for (let i = 0; i < 预期像素字节数; i += 4) {
    const 偏移 = 头部字节数 + i;
    像素数据[i + 0] = 字节数组[偏移 + 2]; // R
    像素数据[i + 1] = 字节数组[偏移 + 1]; // G
    像素数据[i + 2] = 字节数组[偏移 + 0]; // B
    像素数据[i + 3] = 字节数组[偏移 + 3]; // A
  }
  return { 宽度, 高度, 数据: 像素数据 };
}

